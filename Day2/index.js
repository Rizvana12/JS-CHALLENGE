//Arithmetic Operations
let a=10;
let b=7;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

//Assignment Operators

let c=6;
c+=1;
console.log(c);
c-=5;
console.log(c);

//Comparison operator

console.log(a>b);
console.log(a<b);
console.log(a>=b);
console.log(a<=b);
console.log(a==b);
console.log(a===b);

//Logical Operator

let k=3;
let l=5;
let m=8;
let n=10;

console.log(k>l&&m>n);
console.log(k>l||m>n);
console.log(!k);

//Ternary Operator

console.log(k>0?"Positive":"Negative");
